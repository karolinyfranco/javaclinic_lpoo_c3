    private void btnListarTodosPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarTodosPActionPerformed
        ArrayList<Paciente> lista = controllerP.listar();

        DefaultTableModel modelo = (DefaultTableModel) tabelaPaciente.getModel();
        modelo.setRowCount(0);

        for (Paciente p : lista) {
            modelo.addRow(new Object[]{
                p.getCpf(),
                p.getNome(),
                p.getEmail(),
                p.getTelefone(),
                p.getEndereco()
            });
        }
    }//GEN-LAST:event_btnListarTodosPActionPerformed

